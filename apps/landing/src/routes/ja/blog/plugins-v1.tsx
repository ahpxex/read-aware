import { Link, createFileRoute } from "@tanstack/react-router";
import { BlogPost } from "../../../components/BlogPost";
import { getPost } from "../../../lib/posts";
import { MARKETPLACE_REPO_URL } from "../../../lib/site";

const SLUG = "plugins-v1";

export const Route = createFileRoute("/ja/blog/plugins-v1")({
  head: () => {
    const post = getPost(SLUG);
    return {
      meta: [
        { title: `${post.text.ja.title} — ReadAware ブログ` },
        { name: "description", content: post.text.ja.description },
      ],
    };
  },
  component: () => (
    <BlogPost slug={SLUG} locale="ja">
      <p>
        ReadAwareにプラグインシステムが加わりました。プラグインはひと晩で書けます。開発中はフォルダからそのままインストールでき、完成したらコミュニティマーケットプレイスに提出すれば、誰でもワンクリックでインストールできるようになります。
      </p>

      <h2>何が作れるのか</h2>
      <p>
        プラグインはマニフェストとひとつのJavaScriptモジュールを含むフォルダで、次のものを追加できます。
      </p>
      <ul>
        <li>
          <strong>選択テキストアクション</strong>——どの本でもテキストを選択して、そこから操作できます。単語をAnkiに送る、一節を翻訳する、引用をメモアプリに投稿する、など。
        </li>
        <li>
          <strong>ヘッダーボタンとページ</strong>——リーダー上部バーのポップオーバーや、本棚に置かれるフルページ。
        </li>
        <li>
          <strong>コマンド</strong>——すべてが自動的にコマンドパレットに並びます。
        </li>
        <li>
          <strong>アシスタント用ツール</strong>——私たちがいちばん大切にしているのがこれです。プラグインは読書エージェントに新しいツールを渡すことができ、会話の途中でいつ呼び出すかはエージェントが判断します。「この中でAnkiのデッキにもう入っている単語はどれ?」という問いに、答えが返るようになります。
        </li>
        <li>
          <strong>ファイルではない本</strong>——コンテンツプロバイダーが章をオンデマンドで供給します。RSSフィードを本棚に置いて、本とまったく同じように読み、ハイライトし、語り合えるということです。
        </li>
      </ul>

      <h2>作りからしてネイティブ</h2>
      <p>
        インターフェースの思想はRaycastから受け継ぎました。プラグインは自前のUIを描画しません。ビューを宣言するだけです——マークダウン、リスト、フォーム、構造化ブロック。それをアプリが自身のデザインシステムで描画します。プラグインのページは組み込みのページと見分けがつきません。同じ部品から組み立てられているからです。プラグイン作者のデザイン作業はゼロで、アプリはひとつの静かな面であり続けます。
      </p>
      <p>
        そして、家具の配置はあなたのものです。プラグインが持ち込むのは機能であって、どのボタンをどこに、どの順で並べるかは「設定 → カスタマイズ」が決めます。新しいプラグインの操作はオーバーフローメニューに届きます。目の前に割り込んではきません。
      </p>

      <h2>信頼について、正直に</h2>
      <p>
        信頼モデルはObsidianと同じもので、それを率直に述べておきます。プラグインはアプリの内側で動き、インストールするということは作者を信頼するということです。マニフェストは権限——ネットワーク、読書データ、AI、クリップボード——を宣言し、APIがそれを強制しますが、これで防げるのは事故であって悪意ではありません。だからアプリは、ファイルがひとつコピーされるより前に、すべての権限を平易な言葉で提示します。マーケットプレイスはすべての提出物を公開の場でレビューし、読めるソースコードを必須とします。暗がりもなければ、「サンドボックスだから安全」という芝居もありません。
      </p>

      <h2>ここから始める</h2>
      <p>
        <Link to="/ja/docs/plugins/api">APIリファレンス</Link>にはコントラクトの全体が記載されています。
        <a href={MARKETPLACE_REPO_URL} target="_blank" rel="noopener noreferrer">
          マーケットプレイスのリポジトリ
        </a>
        にはTypeScriptのテンプレートに加えて、動く実例としての公式プラグインがあります。まずひとつ書いてみてください。良いものができたら、
        <Link to="/ja/docs/plugins/publishing">提出してください</Link>——設定のマーケットプレイスタブは、最初のコミュニティプラグインを待っています。
      </p>
    </BlogPost>
  ),
});
