import { Link, createFileRoute } from "@tanstack/react-router";
import { MARKETPLACE_REPO_URL } from "../../../../lib/site";

export const Route = createFileRoute("/ja/docs/plugins/")({
  head: () => ({
    meta: [
      { title: "プラグインシステム — ReadAware ドキュメント" },
      {
        name: "description",
        content:
          "ReadAwareのプラグインにできること、信頼モデルの仕組み、そしてインストールの方法。",
      },
    ],
  }),
  component: PluginsOverviewPage,
});

function PluginsOverviewPage() {
  return (
    <article className="doc-prose">
      <h1>プラグインシステム</h1>
      <p className="lead">
        プラグインは、新しい操作やページ、そして何より読書アシスタントの新しいツールでReadAwareを拡張します。プラグインは小さなJavaScriptモジュールで、そのインターフェイスは常にアプリ自身のデザインシステムで描画されます。だからプラグインの機能は、見た目も使い心地もネイティブになります。
      </p>

      <h2>プラグインが提供できるもの</h2>
      <ul>
        <li>
          <strong>選択アクション</strong> —リーダーのテキスト選択メニューに加わる項目です。単語をAnkiに送る、一節を翻訳する、引用をどこかに保存する、など。
        </li>
        <li>
          <strong>ヘッダーボタン</strong> —リーダーや本棚のヘッダーバーに置くアイコンボタンです。ポップオーバーを開いたり、（本棚では）ページ全体を開いたりできます。
        </li>
        <li>
          <strong>コマンド</strong> —コマンドパレットの項目です。プラグインのアクションはすべて自動的にパレットから実行できます。明示的なコマンドはそこへさらに追加するものです。
        </li>
        <li>
          <strong>エージェントツール</strong> —読書アシスタントがチャット中に呼び出せる関数です。もっとも伸びしろの大きい拡張点で、アシスタントにAnkiのデッキやRSSの未読、あなたが使っている任意のサービスを問い合わせさせることができます。
        </li>
        <li>
          <strong>コンテンツプロバイダー</strong> —章をプラグインがオンデマンドで供給する仮想ブックです。RSSフィードを本棚に置き、ほかの本と同じように読み、注釈し、話題にできます。
        </li>
      </ul>

      <h2>プラグインは仕組みとしてネイティブに見える</h2>
      <p>
        プラグインが自前のHTMLを描画することはありません。マークダウン、リスト、フォーム、いくつかの構造化ブロックという小さな語彙でビューを宣言し、アプリが自身のコンポーネントで描画します。プラグイン作者はピクセル単位の制御を手放す代わりに、デザイン作業がゼロになり、アプリはいつまでも一貫した姿を保ちます。
      </p>

      <h2>信頼モデル</h2>
      <p>
        プラグインはアプリと同じJavaScriptコンテキストの中で実行されます。Obsidianと同じ方式で、ブラウザ拡張のサンドボックスとは異なります。守りは、正直に言って次の2層です。
      </p>
      <ul>
        <li>
          <strong>権限</strong> —プラグインのマニフェストが使うもの（ネットワーク、読書データ、AI、クリップボードなど）を宣言し、APIは宣言されたものだけを公開します。これは意図しない越権に対する防御です。
        </li>
        <li>
          <strong>インストールこそが信頼の判断です。</strong>
          何かがコピーまたは実行される前に、アプリはそのプラグインが求める権限をわかりやすい言葉で正確に示し、あなたの同意を待ちます。ソフトウェアをインストールするのと同じ心構えで、プラグインをインストールしてください。
        </li>
      </ul>
      <p>
        アプリ自体のアーキテクチャが影響範囲を限定します。プラグインのストレージはアプリのデータディレクトリの中で名前空間により分離され、デスクトップシェルは任意のファイルシステムアクセスを許可しません。
      </p>

      <h2>プラグインをインストールする</h2>
      <ul>
        <li>
          <strong>マーケットプレイス</strong> — 「設定 → プラグイン →
          マーケットプレイス」には、公開
          <a
            href={MARKETPLACE_REPO_URL}
            target="_blank"
            rel="noopener noreferrer"
          >
            レジストリ
          </a>
          にあるコミュニティプラグインが並びます。インストールはワンクリックで、先に権限の要約が表示されます。
        </li>
        <li>
          <strong>フォルダから</strong> — 「設定 →
          プラグイン」では、任意のローカルフォルダのプラグインをインストールできます。これが開発ループです。作業ディレクトリを指定し、変更を反映するには再インストールします。
        </li>
      </ul>

      <h2>レイアウトを決めるのはあなた</h2>
      <p>
        プラグインが提供するのは機能で、ボタンの置き場所を決めるのはあなたです。「設定 →
        カスタマイズ」で各サーフェス（本棚ヘッダー、リーダーヘッダー、選択メニュー）を整えられます。項目を表示行とオーバーフローメニューの間でドラッグし、並べ替え、既定に戻すこともできます。新しいプラグインのアクションは、まず静かにオーバーフローメニューに入り、そしてすべての操作は常にコマンドパレットから実行できます。
      </p>

      <h2>自分で書く</h2>
      <p>
        プラグインは、<code>manifest.json</code>と1つの<code>main.js</code>
        を持つフォルダです。契約の全体は<Link to="/ja/docs/plugins/api">
        APIリファレンス</Link>が、マーケットプレイスへの出し方は
        <Link to="/ja/docs/plugins/publishing">公開する</Link>が説明します。
      </p>
    </article>
  );
}
